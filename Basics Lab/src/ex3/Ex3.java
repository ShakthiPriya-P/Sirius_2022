package ex3;

public class Ex3 {
	public static void main(String[] args) {
		int a=10;
		float b=3;
		double c=5;
		
		System.out.println("a: "+a);
		System.out.println("b: "+b);
		System.out.println("c: "+c);
		
		System.out.println("Float to int b :"+(int)b);
		System.out.println("Int to float a :"+(float)a);
		System.out.println("Double to String c :"+String.valueOf(c));
		
		
		
	}
}
